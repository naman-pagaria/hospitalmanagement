def patient_signup_view(request):
    userForm = forms.PatientUserForm()
    patientForm = forms.PatientForm()
    mydict = {'userForm': userForm, 'patientForm': patientForm}
    if request.method == 'POST':
        userForm = forms.PatientUserForm(request.POST)
        patientForm = forms.PatientForm(request.POST, request.FILES)
        if userForm.is_valid() and patientForm.is_valid():
            user = userForm.save(commit=False)
            raw_password = userForm.cleaned_data.get('password') or userForm.cleaned_data.get('password1')
            if raw_password:
                user.set_password(raw_password)
            user.save()

            patient = patientForm.save(commit=False)
            patient.user = user
            patient.status = True  # auto-approve so login works immediately

            # If assignedDoctorId isn't posted, assign the first active doctor if available; else 0
            assigned_from_post = request.POST.get('assignedDoctorId')
            if assigned_from_post:
                patient.assignedDoctorId = assigned_from_post
            else:
                doc_user_id = (
                    models.Doctor.objects.filter(status=True)
                    .values_list('user_id', flat=True)
                    .first()
                )
                patient.assignedDoctorId = doc_user_id or 0

            patient.save()

            my_patient_group = Group.objects.get_or_create(name='PATIENT')
            my_patient_group[0].user_set.add(user)

            return redirect(f"{reverse('patientlogin')}?next={reverse('patient-dashboard')}")
    return render(request, 'hospital/patientsignup.html', context=mydict)






#-----------for checking user is doctor , patient or admin(by sumit)
def is_admin(user):
    return user.groups.filter(name='ADMIN').exists()
def is_doctor(user):
    return user.groups.filter(name='DOCTOR').exists()
def is_patient(user):
    return user.groups.filter(name='PATIENT').exists()

# Helper to redirect to the correct dashboard based on user role
def redirect_to_dashboard(user):
    if is_admin(user):
        return redirect('admin-dashboard')
    if is_doctor(user):
        return redirect('doctor-dashboard')
    if is_patient(user):
        return redirect('patient-dashboard')
    # Fallback if user has no group
    return redirect('home')








