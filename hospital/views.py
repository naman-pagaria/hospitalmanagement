@login_required(login_url='doctorlogin')
@user_passes_test(is_doctor)
def doctor_dashboard_view(request):
    doctor_user_id = request.user.id

    # Card metrics
    patientcount = models.Patient.objects.filter(status=True, assignedDoctorId=doctor_user_id).count()
    appointmentcount = models.Appointment.objects.filter(doctorId=doctor_user_id).count()
    patientdischarged = (
        models.PatientDischargeDetails.objects.filter(assignedDoctorName=request.user.first_name)
        .distinct()
        .count()
    )

    # Pull the latest 6 appointments for this doctor (both approved and pending)
    appts = list(
        models.Appointment.objects.filter(doctorId=doctor_user_id)
        .order_by('-id')[:6]
    )

    # Map Appointment.patientId (which stores the Patient's user_id) to Patient objects
    patient_ids = [a.patientId for a in appts]
    patient_map = {p.user_id: p for p in models.Patient.objects.filter(user_id__in=patient_ids)}

    # Build rows with plain strings so templates never have to unpack tuples
    recent_rows = []
    for a in appts:
        p = patient_map.get(a.patientId)
        # Patient name fallback order: Patient object -> stored name on appointment -> "â€”"
        patient_name = (p.get_name if p else None) or getattr(a, 'patientName', None) or 'â€”'

        # Find a date-like field on the appointment to display
        date_val = None
        for fname in ('appointmentDate', 'date', 'created_at', 'created', 'time'):
            if hasattr(a, fname):
                date_val = getattr(a, fname)
                break
        # As a last resort show nothing rather than crashing
        date_display = date_val if date_val is not None else 'â€”'

        status_label = 'Approved' if getattr(a, 'status', False) else 'Pending'
        is_pending = not getattr(a, 'status', False)
        description = getattr(a, 'description', '') or 'â€”'

        recent_rows.append({
            'patient_name': patient_name,
            'date': date_display,
            'status': status_label,
            'is_pending': is_pending,
            'description': description,
        })

    context = {
        # Card metrics used by the header cards
        'patientcount': patientcount,
        'appointmentcount': appointmentcount,
        'patientdischarged': patientdischarged,

        # Old key kept for backward-compatibility (tuple of (appointment, patient))
        'appointments': [(a, patient_map.get(a.patientId)) for a in appts],

        # New, template-friendly rows (strings) for the dashboard table
        'recent_rows': recent_rows,

        # Doctor object for avatar/profile in the layout
        'doctor': models.Doctor.objects.get(user_id=doctor_user_id),
    }
    return render(request, 'hospital/doctor_dashboard.html', context=context)



